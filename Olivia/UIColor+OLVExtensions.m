#import "UIColor+OLVExtensions.h"

@implementation UIColor (OLVExtensions)

+ (UIColor *)healthygreenColor {
    return UIColorFromRGB(0x00a300);
}

+ (UIColor *)okYellowColor {
    return UIColorFromRGB(0xc29b08);
}

+ (UIColor *)mediumOrangeColor {
    return UIColorFromRGB(0xFFC500);
}

+ (UIColor *)dangerRedColor {
    return UIColorFromRGB(0xFF4C43);
}

@end
